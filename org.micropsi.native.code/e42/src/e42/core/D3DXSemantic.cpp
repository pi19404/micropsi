#include "stdafx.h"

#include "e42/core/D3DXSemantic.h"

//-----------------------------------------------------------------------------------------------------------------------------------------
CD3DXSemantic::CD3DXSemantic()
{
}
//-----------------------------------------------------------------------------------------------------------------------------------------
CD3DXSemantic::~CD3DXSemantic()
{
}
//-----------------------------------------------------------------------------------------------------------------------------------------
CD3DXSemantic::CD3DXSemantic(const D3DXSEMANTIC& s)
{
    Usage = s.Usage;
    UsageIndex = s.UsageIndex;
}
//-----------------------------------------------------------------------------------------------------------------------------------------
CD3DXSemantic::CD3DXSemantic(D3DDECLUSAGE eUsage, DWORD dwUsageIndex)
{
    Usage = eUsage;
    UsageIndex = dwUsageIndex;
}
//-----------------------------------------------------------------------------------------------------------------------------------------
CD3DXSemantic& 
CD3DXSemantic::operator=(const D3DXSEMANTIC& s)
{
    Usage = s.Usage;
    UsageIndex = s.UsageIndex;

    return *this;
}
//-----------------------------------------------------------------------------------------------------------------------------------------
bool 
CD3DXSemantic::operator==(const D3DXSEMANTIC& s) const
{
    return 
        (Usage == s.Usage) &&
        (UsageIndex == s.UsageIndex);
}
//-----------------------------------------------------------------------------------------------------------------------------------------
bool 
CD3DXSemantic::operator!=(const D3DXSEMANTIC& s) const
{
    return !operator==(s);
}
//-----------------------------------------------------------------------------------------------------------------------------------------
CD3DXSemantic::operator D3DXSEMANTIC()
{
    return *this;
}
