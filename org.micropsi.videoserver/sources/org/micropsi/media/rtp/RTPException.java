package org.micropsi.media.rtp;

public class RTPException extends Exception {

	public RTPException() {
		super();
	}

	public RTPException(String message) {
		super(message);
	}

	public RTPException(String message, Throwable cause) {
		super(message, cause);
	}

	public RTPException(Throwable cause) {
		super(cause);
	}

}
